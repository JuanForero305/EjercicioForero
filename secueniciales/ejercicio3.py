"""
Construya un algoritmo que calcule el perímetro y el área de un rectángulo dada su base y su altura.
La base y la altura deberán ser almacenadas previamente en dos variables respectivamente.
"""
base=int(input("digite un valor:"))
altura=int(input("digite otro valor:"))

#forma imprimir 1
perimetro=2*(base+altura)
area=(base*altura)
print(f'El perimetro es igual {perimetro}, y el area es igual a {area}')


